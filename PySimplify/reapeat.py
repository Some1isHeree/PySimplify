
def strrepeat(hmt, string):
    for i in range(hmt):
        print(str(string))

def intrepeat(hmt, intiger):
    for i in range(hmt):
        print(int(intiger))
        
def flrepeat(hmt, fl):
    for i in range(hmt):
        print(float(fl))


def px2y(x, y):
    y = y + 1
    for i in range(x, y):
        print(i)